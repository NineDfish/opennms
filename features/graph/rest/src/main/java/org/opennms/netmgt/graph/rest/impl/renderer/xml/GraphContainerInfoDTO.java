package org.opennms.netmgt.graph.rest.impl.renderer.xml;

import java.util.ArrayList;
import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlElementWrapper;

@XmlAccessorType(XmlAccessType.NONE)
public class GraphContainerInfoDTO {

    @XmlAttribute(name="id", required=true)
    private String id;

    @XmlAttribute(name="label")
    private String label;

    @XmlAttribute(name="description")
    private String description;

    @XmlElement(name="graph")
    @XmlElementWrapper(name="graphs")
    private List<GraphInfoDTO> graphs = new ArrayList<>();

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public List<GraphInfoDTO> getGraphs() {
        return graphs;
    }

    public void setGraphs(List<GraphInfoDTO> graphs) {
        this.graphs = graphs;
    }
}
